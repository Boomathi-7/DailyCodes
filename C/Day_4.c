//Arithmatic Operators

#include <stdio.h>
int main(){
  int num1, num2;
  printf("Enter num1: ");
  scanf("%d", &num1);
  printf("Enter num2: ");
  scanf("%d", &num2);
  printf("Addition is %d\n", num1+num2);
  printf("Difference is %d\n", num1-num2);
  printf("Multiplication is %d\n", num1*num2);
  printf("Division is %d\n", num1/num2);
  printf("Remainder on division is %d\n", num1%num2);
  return 0;
}
